function promedio(pnota1,pnota2,pnota3){
    let nota1=pnota1;
    let nota2=pnota2;
    let nota3=pnota3;
    let suma;
    let promedio;
    suma=nota1+nota2+nota3;
    promedio=suma/3;
    return promedio;
}

