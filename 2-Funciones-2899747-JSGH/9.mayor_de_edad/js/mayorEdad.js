function mayorEdad(pedad){
    let edad=pedad;
    if(edad>17){
        return "Edad: "+"("+edad+") "+"Usted es mayor de edad";
    }else{
        return "Edad: "+"("+edad+") "+"Usted es menor de edad";
    }
}

